#include "../libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result_string;
	int		len1;
	int		len2;

	if (s1 == 0 || s2 == 0)
		return (0);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result_string = malloc(len1 + len2 + 1);
	if (result_string == 0)
		return (0);
	ft_strlcpy(result_string, s1, len1 + 1);
	ft_strlcpy(result_string + len1, s2, len2 + 1);
	return (result_string);
}
