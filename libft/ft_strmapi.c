#include "../libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				length;
	char			*new;

	if (s == 0 || f == 0)
		return (0);
	length = ft_strlen(s);
	new = (char *)malloc((sizeof(char)) * (length + 1));
	if (new == 0)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
